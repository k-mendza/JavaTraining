package udemy.javaclass.variables.FloatAndDouble;

public class Main {

    public static void main(String[] args)
	{
    // this is comment
	// declaration of INT variable
	int myIntValue = 5 / 3;
	// declaration of FLOAT variable
	float myFloatValue = 5f  / 3f;
	// declaration of DOUBLE variable
	double myDoubleValue = 5d / 3d;
	// to type print faster - sout
	// sout of all variables
	System.out.println("myIntValue = " + myIntValue);
	System.out.println("myFloatValue = " + myFloatValue);
	// Double has 2x the precision of float
	System.out.println("myDoubleValue = " + myDoubleValue);
    }
}
